#   2-4. Name Cases: Use a variable to represent a person’s name, and then print
#   that person’s name in lowercase, uppercase, and title case.

nombre = input('Hola, cual es tu nombre? ')
print(f'Hola {nombre.lower()}! Bienvenido!!!')  # Muestra el texto en minusculas
print(f'Hola {nombre.upper()}! Bienvenido!!!')  # Muestra el texto en mayusculas
print(f'Hola {nombre.title()}! Bienvenido!!!')  # Muestra el texto con la primer letra en mayuscula
