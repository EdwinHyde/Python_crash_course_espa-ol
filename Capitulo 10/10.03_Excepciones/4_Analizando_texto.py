# Introduzcamos el texto "Alicia en el pais de las maravillas" e intentemos contar el numero de palabras del texto.
# Hacemos uso del metodo split(), el cual puede construir una lista de palabras desde un string.

titulo = 'Alicia en el pais de las maravillas'
print(titulo.split())  # Este metodo separa un string donde encuentre un espacio y almacena cada parte en una lista.